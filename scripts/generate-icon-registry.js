#!/usr/bin/env node

/**
 * Generates the icon registry (React component map) and icon catalog (JSON for system prompt)
 * from installed @atlaskit/icon and @atlaskit/icon-lab packages.
 *
 * Usage: node scripts/generate-icon-registry.js
 *
 * Outputs:
 *   - components/projects/shared/components/icon-registry.tsx
 *   - backend/lib/generated-icon-catalog.json
 */

const fs = require("node:fs");
const path = require("node:path");

const ROOT = path.resolve(__dirname, "..");
const CORE_DIR = path.join(ROOT, "node_modules/@atlaskit/icon/core");
const LAB_DIR = path.join(ROOT, "node_modules/@atlaskit/icon-lab/core");

const REGISTRY_OUT = path.join(
	ROOT,
	"components/projects/shared/components/icon-registry.tsx",
);
const CATALOG_OUT = path.join(ROOT, "backend/lib/generated-icon-catalog.json");

// Lab icon prefixes to exclude (utility/shape/alignment icons not useful for content hints)
const LAB_EXCLUDE_PREFIXES = [
	"diagram-",
	"stroke-",
	"shape-",
	"align-",
	"node-",
	"corner-radius-",
];

function getIconNames(dir, filter) {
	if (!fs.existsSync(dir)) return [];
	return fs
		.readdirSync(dir)
		.filter((f) => f.endsWith(".js") && !f.endsWith(".d.js"))
		.map((f) => f.replace(/\.js$/, ""))
		.filter(filter ?? (() => true))
		.sort();
}

function toImportName(kebab) {
	// chart-bar -> ChartBarIcon
	return (
		kebab
			.split("-")
			.map((s) => s.charAt(0).toUpperCase() + s.slice(1))
			.join("") + "Icon"
	);
}

function main() {
	// Collect core icons
	const coreNames = getIconNames(CORE_DIR);
	const coreSet = new Set(coreNames);

	// Collect lab icons, excluding certain prefixes and names that collide with core
	const labNames = getIconNames(LAB_DIR, (name) => {
		if (LAB_EXCLUDE_PREFIXES.some((p) => name.startsWith(p))) return false;
		// Skip lab icons that duplicate core names
		if (coreSet.has(name)) return false;
		return true;
	});

	// Build import lines and registry entries
	const importLines = [];
	const registryEntries = [];
	const allNames = [];

	for (const name of coreNames) {
		const importName = toImportName(name);
		importLines.push(
			`import ${importName} from "@atlaskit/icon/core/${name}";`,
		);
		registryEntries.push(`\t["${name}", ${importName}],`);
		allNames.push(name);
	}

	for (const name of labNames) {
		// Use Lab prefix to avoid import name collisions with differently-named core icons
		const importName = "Lab" + toImportName(name);
		importLines.push(
			`import ${importName} from "@atlaskit/icon-lab/core/${name}";`,
		);
		// Lab icons are namespaced with lab/ prefix
		registryEntries.push(`\t["lab/${name}", ${importName}],`);
		allNames.push(`lab/${name}`);
	}

	// Generate registry file
	const registryContent = [
		"// Auto-generated by scripts/generate-icon-registry.js — do not edit manually",
		`import type { NewCoreIconProps } from "@atlaskit/icon/base-new";`,
		"",
		...importLines,
		"",
		"type IconComponent = (props: NewCoreIconProps) => JSX.Element;",
		"",
		"export const ICON_REGISTRY: ReadonlyMap<string, IconComponent> = new Map([",
		...registryEntries,
		"]);",
		"",
	].join("\n");

	fs.writeFileSync(REGISTRY_OUT, registryContent, "utf-8");
	console.log(
		`Written ${REGISTRY_OUT} (${coreNames.length} core + ${labNames.length} lab = ${allNames.length} icons)`,
	);

	// Generate catalog JSON
	const catalog = {
		names: allNames,
		count: allNames.length,
	};

	fs.writeFileSync(CATALOG_OUT, JSON.stringify(catalog, null, "\t") + "\n", "utf-8");
	console.log(`Written ${CATALOG_OUT} (${catalog.count} icon names)`);
}

main();
